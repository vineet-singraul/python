# 1. **Map Question:**  
#    Given a list of numbers `[1, 2, 3, 4, 5]`, use the `map()` function to square each number and return a list   
#    of   squared values.

# 2. **Filter Question:**  
#    Given a list of numbers `[10, 15, 20, 25, 30]`, use the `filter()` function to get only the numbers that 
#    are divisible by 5.

# 3. **Reduce Question:**  
#    Given a list `[1, 2, 3, 4, 5]`, use the `reduce()` function from `functools` to find the product of all 
#     numbers in the list.

# 4. **Map & Filter Combined Question:**  
#    Given a list of words `["apple", "banana", "cherry", "date"]`, use `map()` to convert them to uppercase
#    and `filter()` to keep only the words with more than five letters.

# 5. **Reduce with a Custom Function Question:**  
#    Given a list `[100, 50, 25, 5]`, use `reduce()` to perform a subtraction operation from left to right.

# 6. **Lambda with Map, Filter, and Reduce Question:**  
#    Use `map()`, `filter()`, and `reduce()` with lambda functions to process a list
